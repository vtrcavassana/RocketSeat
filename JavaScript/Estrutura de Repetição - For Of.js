// Estrutura de Repetição - For...Of

let nome = 'Vitu'
let nomes = ['Virtu', 'Vitola', 'Vitoca']

// "For...Of" -> utilizado para iterar sobre valor do objeto
for(let letra of nome){
    console.log(letra)
}

for(let elemento of nomes){
    console.log(elemento)
}

// Veja For..In para iterar sobre chaves do objeto