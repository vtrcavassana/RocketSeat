// Estrutura de Repetição - For...In

let nome = 'Vitu'
let nomes = ['Virtu', 'Vitola', 'Vitoca']

// "For...In" -> utilizado para iterar sobre chave do objeto
for(let letra in nome){
    console.log(letra)
}

for(let elemento in nomes){
    console.log(elemento)
}

// Veja For..Of para iterar sobre valor do objeto